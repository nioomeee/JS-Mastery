// Absolute method
console.log(Math.abs(-12.5)); //12.5
console.log(Math.abs(12.5)); //12.5

// Power method:
// Math.pow(a, b); //a to the power b
console.log(Math.pow(2,4));

// Floor- rounds off to less than or equal integer
console.log(Math.floor(5.9)); //5
console.log(Math.floor(-12.5)); //-13

// Ceil- Ceiling rounds off to more than or equal integer
console.log(Math.ceil(12.5)); //13
console.log(Math.ceil(-12.5)); //-12
console.log(Math.ceil(5.0000000001)); //6

// Math.random() - generates random no. between 0-1 with 1 being exclusive
console.log(Math.random());
