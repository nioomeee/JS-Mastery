let colors = ["red", "yellow", "blue", "orange", "white"];

console.log(colors.slice()); //(5) ['red', 'yellow', 'blue', 'orange', 'white']

console.log(colors.slice(2)); //(3) ['blue', 'orange', 'white']

console.log(colors.slice(2, 4)); //(2) ['blue', 'orange']

console.log(colors.slice(-3)); //(3) ['blue', 'orange', 'white']






